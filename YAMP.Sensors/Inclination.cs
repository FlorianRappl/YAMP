namespace YAMP.Sensors
{
    using System;

    /// <summary>
    /// The inclination.
    /// </summary>
    public struct Inclination
    {
        /// <summary>
        /// Gets or sets the pitch.
        /// </summary>
        public Double Pitch;

        /// <summary>
        /// Gets or sets the roll.
        /// </summary>
        public Double Roll;

        /// <summary>
        /// Gets or sets the yaw.
        /// </summary>
        public Double Yaw;
    }
}
