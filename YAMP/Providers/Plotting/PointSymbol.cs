using System;

namespace YAMP
{
    /// <summary>
    /// Defines the point symbol to use for a plot series.
    /// </summary>
	public enum PointSymbol
	{
        /// <summary>
        /// No symbol will be shown.
        /// </summary>
		None = 0,
        /// <summary>
        /// A circle.
        /// </summary>
		Circle = 1,
        /// <summary>
        /// A square.
        /// </summary>
		Square = 2,
        /// <summary>
        /// A diamond.
        /// </summary>
		Diamond = 3,
        /// <summary>
        /// A triangle.
        /// </summary>
		Triangle = 4,
        /// <summary>
        /// A cross.
        /// </summary>
		Cross = 5,
        /// <summary>
        /// A plus.
        /// </summary>
		Plus = 6,
        /// <summary>
        /// A star.
        /// </summary>
		Star = 7
	}
}
